#include "libft.h"

int get(char *buff)
{
	int(i), (n);
	i = 0;
	n = 0;
	if (buff[0] == '\n')
		n++;
	while (buff[i])
	{
		if (buff[i] == '\n' && buff[i + 1] == '\n')
			n++;
		i++;
	}
	if (n > 0)
		return (1);
	return (0);
}